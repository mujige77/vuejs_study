<template>
    <div>
        <h1>The User Page</h1>
        <hr>
        <p>Loaded Id: {{ id }}</P>
        <button @click="navigateToHome" class="btn btn-primary">Go to home</button>
        <router-view></router-view>
    </div>
</template>

<script>
export default {
    data() {
        return {
            id: this.$route.params.id,
        }
    },
    watch: {
        '$route'(to, from) {
            this.id = to.params.id
        }
    },
    methods: {
        navigateToHome() {
            // this.$router.push('/')
            this.$router.push({name: 'home'})
        }
  }
}
</script>
